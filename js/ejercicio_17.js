// Escribir un programa que calcule el volumen de una esfera.
let radio = parseFloat(prompt("Indique el radio de la esfera"));
let pi = 3.1416
operacion = ((4 / 3) * pi * (radio ** 3));
area = operacion.toFixed(2);
