// Escribir un programa que calcule la velocidad de un proyectil que recorre 2km en 5 minutos, exprese el resultado en metros / segundo.
let distancia = 2000; // 2km en metros
let tiempo = 5 * 60; // 5 minutos convertidos a segundos
operacion = (distancia / tiempo);
velocidad = operacion.toFixed(2);
alert(`La velocidad alcanzada por el proyectil equivale a ${velocidad} M/S`)