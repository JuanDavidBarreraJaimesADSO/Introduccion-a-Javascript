// . Escribir un programa que calcule el área de un triángulo.
let base = parseFloat(prompt("Indique la base del triangulo"));
let altura = parseFloat(prompt("Indique la altura del triangulo"));
operacion = (base * altura) / 2;
alert(`El area del triangulo correspondiente a su base ${base} , y su altura ${altura} es igual a ${operacion}`)